package model;

public class Month {
	private Double income;
	private Double incomeR;
	private Double buyG;
	private Double expenseIn;
	private Double reward;
	private Double expense;
	private Double expenseRes;
	
	public Double getIncome() {
		return income;
	}
	public void setIncome(Double income) {
		this.income = income;
	}
	public Double getIncomeR() {
		return incomeR;
	}
	public void setIncomeR(Double incomeR) {
		this.incomeR = incomeR;
	}
	public Double getBuyG() {
		return buyG;
	}
	public void setBuyG(Double buyG) {
		this.buyG = buyG;
	}
	public Double getExpenseIn() {
		return expenseIn;
	}
	public void setExpenseIn(Double expenseIn) {
		this.expenseIn = expenseIn;
	}
	public Double getReward() {
		return reward;
	}
	public void setReward(Double reward) {
		this.reward = reward;
	}
	public Double getExpense() {
		return expense;
	}
	public void setExpense(Double expense) {
		this.expense = expense;
	}
	public Double getExpenseRes() {
		return expenseRes;
	}
	public void setExpenseRes(Double expenseRes) {
		this.expenseRes = expenseRes;
	}
}
